package com.camp.s1.camping;

public class LayoutDTO {

	private Long layoutNum;
	private Long campNum;
	private String layoutName;
	
	public LayoutDTO() {
		
	}

	public Long getLayoutNum() {
		return layoutNum;
	}

	public void setLayoutNum(Long layoutNum) {
		this.layoutNum = layoutNum;
	}

	public Long getCampNum() {
		return campNum;
	}

	public void setCampNum(Long campNum) {
		this.campNum = campNum;
	}

	public String getLayoutName() {
		return layoutName;
	}

	public void setLayoutName(String layoutName) {
		this.layoutName = layoutName;
	}
	
}
